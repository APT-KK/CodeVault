// Using var (older style)
var greeting = "Hello Hacktoberfest!";
console.log("var - String: " + greeting);

// Using let (block-scoped)
let score = 100;
console.log("let - Number: " + score);

// Using const (constant, block-scoped)
const isActive = true;
console.log("const - Boolean: " + isActive);

// Null and Undefined
let noValue = null;
let notDefined;
console.log("Null: " + noValue);
console.log("Undefined: " + notDefined);

// Object
const user = { name: "Sabin", age: 20 };
console.log("Object - Name: " + user.name + ", Age: " + user.age);

// Array
const tasks = ["Contribute", "Learn", "Share"];
console.log("Array: " + tasks.join(", "));

